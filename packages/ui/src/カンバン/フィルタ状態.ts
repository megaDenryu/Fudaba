// カンバン上部のフィルタバーが持つ選択状態。種別・担当者・ラベルの3面を合成する
// (状態は列そのものが表現するためここには含めない。DESIGN.md「ラベル」節参照)
export interface カンバンフィルタ状態 {
  readonly 種別: string | null;
  readonly 担当者: string | null;
  readonly ラベル一覧: readonly string[];
}

export const 初期カンバンフィルタ状態: カンバンフィルタ状態 = {
  種別: null,
  担当者: null,
  ラベル一覧: [],
};

// 種別・担当者は単一選択（同じ値をもう一度選ぶと解除）。ラベルは複数選択（AND条件で絞り込む）
export function 種別選択を切り替える(
  フィルタ: カンバンフィルタ状態,
  種別: string,
): カンバンフィルタ状態 {
  return { ...フィルタ, 種別: フィルタ.種別 === 種別 ? null : 種別 };
}

export function 担当者選択を切り替える(
  フィルタ: カンバンフィルタ状態,
  担当者: string,
): カンバンフィルタ状態 {
  return { ...フィルタ, 担当者: フィルタ.担当者 === 担当者 ? null : 担当者 };
}

export function ラベル選択を切り替える(
  フィルタ: カンバンフィルタ状態,
  ラベル: string,
): カンバンフィルタ状態 {
  const 選択中 = フィルタ.ラベル一覧.includes(ラベル);
  return {
    ...フィルタ,
    ラベル一覧: 選択中
      ? フィルタ.ラベル一覧.filter((値) => 値 !== ラベル)
      : [...フィルタ.ラベル一覧, ラベル],
  };
}
