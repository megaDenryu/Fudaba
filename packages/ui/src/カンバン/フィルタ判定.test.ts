import { describe, expect, it } from "vitest";
import type { 札DTO } from "../通信/札型";
import { 初期カンバンフィルタ状態 } from "./フィルタ状態";
import { 札がフィルタに一致するか } from "./フィルタ判定";

function 札を作る(上書き: Partial<札DTO>): 札DTO {
  return {
    id: 1,
    種別: "タスク",
    タイトル: "サンプル",
    本文: "本文",
    状態: "未着手",
    担当者: null,
    作成者: "claude",
    ルーム名: null,
    ラベル一覧: [],
    添付一覧: [],
    チェック項目一覧: [],
    分解推奨: false,
    作成時刻: "2026-07-14T00:00:00.000Z",
    更新時刻: "2026-07-14T00:00:00.000Z",
    ...上書き,
  };
}

describe("札がフィルタに一致するか", () => {
  it("初期フィルタ(全て未選択)は全ての札に一致する", () => {
    expect(札がフィルタに一致するか(札を作る({}), 初期カンバンフィルタ状態)).toBe(true);
  });

  it("種別フィルタは一致する種別だけを通す", () => {
    const フィルタ = { ...初期カンバンフィルタ状態, 種別: "バグ" };
    expect(札がフィルタに一致するか(札を作る({ 種別: "バグ" }), フィルタ)).toBe(true);
    expect(札がフィルタに一致するか(札を作る({ 種別: "タスク" }), フィルタ)).toBe(false);
  });

  it("担当者フィルタは未割当をnullでなく空文字として扱った札には一致しない", () => {
    const フィルタ = { ...初期カンバンフィルタ状態, 担当者: "codex" };
    expect(札がフィルタに一致するか(札を作る({ 担当者: "codex" }), フィルタ)).toBe(true);
    expect(札がフィルタに一致するか(札を作る({ 担当者: null }), フィルタ)).toBe(false);
  });

  it("ラベルフィルタは選択した全ラベルを持つ札だけを通す(AND条件)", () => {
    const フィルタ = { ...初期カンバンフィルタ状態, ラベル一覧: ["fudaba", "urgent"] };
    expect(
      札がフィルタに一致するか(札を作る({ ラベル一覧: ["fudaba", "urgent", "extra"] }), フィルタ),
    ).toBe(true);
    expect(札がフィルタに一致するか(札を作る({ ラベル一覧: ["fudaba"] }), フィルタ)).toBe(false);
  });

  it("種別・担当者・ラベルの複数フィルタは合成される(AND)", () => {
    const フィルタ = { 種別: "バグ", 担当者: "codex", ラベル一覧: ["urgent"] };
    expect(
      札がフィルタに一致するか(
        札を作る({ 種別: "バグ", 担当者: "codex", ラベル一覧: ["urgent"] }),
        フィルタ,
      ),
    ).toBe(true);
    expect(
      札がフィルタに一致するか(
        札を作る({ 種別: "バグ", 担当者: "claude", ラベル一覧: ["urgent"] }),
        フィルタ,
      ),
    ).toBe(false);
  });
});
