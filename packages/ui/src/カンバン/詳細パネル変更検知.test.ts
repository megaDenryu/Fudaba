import { describe, expect, it } from "vitest";
import type { 札DTO } from "../通信/札型";
import { 編集値に変更があるか } from "./詳細パネル変更検知";

const 元札: 札DTO = {
  id: 1,
  種別: "タスク",
  タイトル: "サンプル",
  本文: "本文",
  状態: "未着手",
  担当者: null,
  作成者: "claude",
  ルーム名: null,
  ラベル一覧: ["fudaba"],
  添付一覧: [],
  チェック項目一覧: [],
  分解推奨: false,
  作成時刻: "2026-07-14T00:00:00.000Z",
  更新時刻: "2026-07-14T00:00:00.000Z",
};

describe("編集値に変更があるか", () => {
  it("元の札と同じ内容なら変更なしと判定する", () => {
    const 判定 = 編集値に変更があるか(元札, {
      種別: 元札.種別,
      タイトル: 元札.タイトル,
      本文: 元札.本文,
      状態: 元札.状態,
      担当者: "",
      ラベル一覧: "fudaba",
    });
    expect(判定).toBe(false);
  });

  it("タイトルが変わっていれば変更ありと判定する", () => {
    const 判定 = 編集値に変更があるか(元札, {
      種別: 元札.種別,
      タイトル: "変更後タイトル",
      本文: 元札.本文,
      状態: 元札.状態,
      担当者: "",
      ラベル一覧: "fudaba",
    });
    expect(判定).toBe(true);
  });

  it("状態や本文の変更も検知する", () => {
    expect(
      編集値に変更があるか(元札, {
        種別: 元札.種別,
        タイトル: 元札.タイトル,
        本文: "書き換えた本文",
        状態: 元札.状態,
        担当者: "",
        ラベル一覧: "fudaba",
      }),
    ).toBe(true);
    expect(
      編集値に変更があるか(元札, {
        種別: 元札.種別,
        タイトル: 元札.タイトル,
        本文: 元札.本文,
        状態: "完了",
        担当者: "",
        ラベル一覧: "fudaba",
      }),
    ).toBe(true);
  });

  it("種別の変更も検知する", () => {
    const 判定 = 編集値に変更があるか(元札, {
      種別: "バグ",
      タイトル: 元札.タイトル,
      本文: 元札.本文,
      状態: 元札.状態,
      担当者: "",
      ラベル一覧: "fudaba",
    });
    expect(判定).toBe(true);
  });

  it("担当者未設定(null)の札に担当者を入力すると変更ありと判定する", () => {
    const 判定 = 編集値に変更があるか(元札, {
      種別: 元札.種別,
      タイトル: 元札.タイトル,
      本文: 元札.本文,
      状態: 元札.状態,
      担当者: "誰か",
      ラベル一覧: "fudaba",
    });
    expect(判定).toBe(true);
  });

  it("ラベル一覧が同じ内容なら順序が違っても変更なしと判定する", () => {
    const 元札2 = { ...元札, ラベル一覧: ["fudaba", "jimbo"] };
    const 判定 = 編集値に変更があるか(元札2, {
      種別: 元札2.種別,
      タイトル: 元札2.タイトル,
      本文: 元札2.本文,
      状態: 元札2.状態,
      担当者: "",
      ラベル一覧: "jimbo, fudaba",
    });
    expect(判定).toBe(false);
  });

  it("ラベル一覧が増えていれば変更ありと判定する", () => {
    const 判定 = 編集値に変更があるか(元札, {
      種別: 元札.種別,
      タイトル: 元札.タイトル,
      本文: 元札.本文,
      状態: 元札.状態,
      担当者: "",
      ラベル一覧: "fudaba, jimbo",
    });
    expect(判定).toBe(true);
  });

  it("ラベル一覧が空になれば変更ありと判定する", () => {
    const 判定 = 編集値に変更があるか(元札, {
      種別: 元札.種別,
      タイトル: 元札.タイトル,
      本文: 元札.本文,
      状態: 元札.状態,
      担当者: "",
      ラベル一覧: "",
    });
    expect(判定).toBe(true);
  });

  it("チェック項目の追加や完了変更を検知する", () => {
    const 項目 = { id: "task-1", 本文: "確認する", 完了: false };
    expect(編集値に変更があるか(元札, {
      種別: 元札.種別, タイトル: 元札.タイトル, 本文: 元札.本文,
      状態: 元札.状態, 担当者: "", ラベル一覧: "fudaba", チェック項目一覧: [項目],
    })).toBe(true);
    expect(編集値に変更があるか({ ...元札, チェック項目一覧: [項目] }, {
      種別: 元札.種別, タイトル: 元札.タイトル, 本文: 元札.本文,
      状態: 元札.状態, 担当者: "", ラベル一覧: "fudaba",
      チェック項目一覧: [{ ...項目, 完了: true }],
    })).toBe(true);
  });
});
