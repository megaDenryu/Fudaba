import { describe, expect, it } from "vitest";
import type { 札DTO } from "../通信/札型";
import { 編集値に変更があるか } from "./詳細パネル変更検知";

const 元札: 札DTO = {
  id: 1,
  種別: "タスク",
  タイトル: "サンプル",
  本文: "本文",
  状態: "未着手",
  担当者: null,
  作成者: "claude",
  ルーム名: null,
  作成時刻: "2026-07-14T00:00:00.000Z",
  更新時刻: "2026-07-14T00:00:00.000Z",
};

describe("編集値に変更があるか", () => {
  it("元の札と同じ内容なら変更なしと判定する", () => {
    const 判定 = 編集値に変更があるか(元札, {
      タイトル: 元札.タイトル,
      本文: 元札.本文,
      状態: 元札.状態,
      担当者: "",
    });
    expect(判定).toBe(false);
  });

  it("タイトルが変わっていれば変更ありと判定する", () => {
    const 判定 = 編集値に変更があるか(元札, {
      タイトル: "変更後タイトル",
      本文: 元札.本文,
      状態: 元札.状態,
      担当者: "",
    });
    expect(判定).toBe(true);
  });

  it("状態や本文の変更も検知する", () => {
    expect(
      編集値に変更があるか(元札, {
        タイトル: 元札.タイトル,
        本文: "書き換えた本文",
        状態: 元札.状態,
        担当者: "",
      }),
    ).toBe(true);
    expect(
      編集値に変更があるか(元札, {
        タイトル: 元札.タイトル,
        本文: 元札.本文,
        状態: "完了",
        担当者: "",
      }),
    ).toBe(true);
  });

  it("担当者未設定(null)の札に担当者を入力すると変更ありと判定する", () => {
    const 判定 = 編集値に変更があるか(元札, {
      タイトル: 元札.タイトル,
      本文: 元札.本文,
      状態: 元札.状態,
      担当者: "誰か",
    });
    expect(判定).toBe(true);
  });
});
