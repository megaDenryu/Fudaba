import { describe, expect, it } from "vitest";
import type { キャラDTO } from "../通信/キャラ型";
import type { 稼働表明DTO } from "../通信/稼働型";
import type { 札DTO } from "../通信/札型";
import { AI担当者名集合を作る, 稼働状態マップを作る, 淀んでいるか } from "./淀み判定";

function 札を作る(上書き: Partial<札DTO>): 札DTO {
  return {
    id: 1,
    種別: "タスク",
    タイトル: "サンプル",
    本文: "本文",
    状態: "進行中",
    担当者: "claude",
    作成者: "human",
    ルーム名: null,
    ラベル一覧: [],
    添付一覧: [],
    作成時刻: "2026-07-14T00:00:00.000Z",
    更新時刻: "2026-07-14T00:00:00.000Z",
    ...上書き,
  };
}

function キャラを作る(上書き: Partial<キャラDTO>): キャラDTO {
  return {
    名前: "claude",
    種別: "AI",
    プロンプト: "",
    アイコンdataUrl: "",
    行動パターンメモ: "",
    作成者: "human",
    作成時刻: "2026-07-14T00:00:00.000Z",
    更新時刻: "2026-07-14T00:00:00.000Z",
    ...上書き,
  };
}

function 稼働表明を作る(上書き: Partial<稼働表明DTO>): 稼働表明DTO {
  return {
    名前: "claude",
    状態: "稼働中",
    現在の作業: null,
    札ID: null,
    更新時刻: "2026-07-14T00:00:00.000Z",
    ...上書き,
  };
}

describe("淀んでいるか", () => {
  it("状態が進行中でなければ淀みなし", () => {
    const AI集合 = AI担当者名集合を作る([キャラを作る({})]);
    const マップ = 稼働状態マップを作る([]);
    expect(淀んでいるか(札を作る({ 状態: "未着手" }), AI集合, マップ)).toBe(false);
  });

  it("担当者が未割当なら淀みなし", () => {
    const AI集合 = AI担当者名集合を作る([キャラを作る({})]);
    const マップ = 稼働状態マップを作る([]);
    expect(淀んでいるか(札を作る({ 担当者: null }), AI集合, マップ)).toBe(false);
  });

  it("担当者がAIキャラ一覧に無ければ淀みなし(人間の担当は判定対象外)", () => {
    const AI集合 = AI担当者名集合を作る([キャラを作る({ 名前: "codex" })]);
    const マップ = 稼働状態マップを作る([]);
    expect(淀んでいるか(札を作る({ 担当者: "claude" }), AI集合, マップ)).toBe(false);
  });

  it("AI担当者のpresenceが未表明なら淀みあり", () => {
    const AI集合 = AI担当者名集合を作る([キャラを作る({})]);
    const マップ = 稼働状態マップを作る([]);
    expect(淀んでいるか(札を作る({}), AI集合, マップ)).toBe(true);
  });

  it("AI担当者のpresenceが不明なら淀みあり", () => {
    const AI集合 = AI担当者名集合を作る([キャラを作る({})]);
    const マップ = 稼働状態マップを作る([稼働表明を作る({ 状態: "不明" })]);
    expect(淀んでいるか(札を作る({}), AI集合, マップ)).toBe(true);
  });

  it("AI担当者のpresenceが稼働中なら淀みなし", () => {
    const AI集合 = AI担当者名集合を作る([キャラを作る({})]);
    const マップ = 稼働状態マップを作る([稼働表明を作る({ 状態: "稼働中" })]);
    expect(淀んでいるか(札を作る({}), AI集合, マップ)).toBe(false);
  });
});
