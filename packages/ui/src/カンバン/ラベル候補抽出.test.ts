import { describe, expect, it } from "vitest";
import type { 札DTO } from "../通信/札型";
import { ラベル候補一覧を抽出する } from "./ラベル候補抽出";

function 札を作る(上書き: Partial<札DTO>): 札DTO {
  return {
    id: 1,
    種別: "タスク",
    タイトル: "サンプル",
    本文: "本文",
    状態: "未着手",
    担当者: null,
    作成者: "claude",
    ルーム名: null,
    ラベル一覧: [],
    添付一覧: [],
    作成時刻: "2026-07-14T00:00:00.000Z",
    更新時刻: "2026-07-14T00:00:00.000Z",
    ...上書き,
  };
}

describe("ラベル候補一覧を抽出する", () => {
  it("全札のラベルを集約する", () => {
    const 候補 = ラベル候補一覧を抽出する([
      札を作る({ id: 1, ラベル一覧: ["fudaba"] }),
      札を作る({ id: 2, ラベル一覧: ["jimbo"] }),
    ]);
    expect(候補).toEqual(["fudaba", "jimbo"]);
  });

  it("重複したラベルは1件にまとめる", () => {
    const 候補 = ラベル候補一覧を抽出する([
      札を作る({ id: 1, ラベル一覧: ["fudaba"] }),
      札を作る({ id: 2, ラベル一覧: ["fudaba"] }),
    ]);
    expect(候補).toEqual(["fudaba"]);
  });

  it("ラベルが無い札からは何も抽出しない", () => {
    expect(ラベル候補一覧を抽出する([札を作る({ ラベル一覧: [] })])).toEqual([]);
  });

  it("空の一覧からは空配列を返す", () => {
    expect(ラベル候補一覧を抽出する([])).toEqual([]);
  });
});
